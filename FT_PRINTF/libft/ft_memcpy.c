#include "../include/libft.h"

void	*ft_memcpy(void *dest, const void *src, unsigned int n)
{
	size_t	i;

	if (!dest)
		return (NULL);
	i = 0;
	while (i < n)
	{
		*(char *)(dest + i) = *(char *)(src + i);
		i++;
	}
	return (dest);
}
